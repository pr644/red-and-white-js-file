let day = Number(prompt("Enter the day"));

if(day === 1){
  alert("Monday");
}else if(day === 2){
   alert("Tuesday");
}else if(day === 3){
   alert("Wednesday");
}else if(day === 4){
  alert("Thursday");
}else if(day === 5){
    alert("Friday"); 
}else if(day === 6){
    alert("Saturday");
}else if(day === 7){
    alert("sunday");
}else{
    alert("invalid input");
}